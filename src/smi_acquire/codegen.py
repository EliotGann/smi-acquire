def to_queueserver_item(samplelist, letter, values=None, *, project_md=None) -> Dict[str, Any]:
    """Return a dict approximating a queueserver ``BPlan`` item for the chosen technique.

    This is the seam where the "emit a script" step is later replaced by submitting to the
    qserver REST API.  Kept deliberately simple: name = entry point, kwargs = sample dicts +
    rendered params.  (The sample bar is serialized as ``from_dicts`` input.)
    """
    spec = _tech.get(letter)
    if spec is None:
        raise KeyError("Technique {!r} has no bar entry point for queueserver".format(letter))
    vals = {**spec.defaults(), **(values or {})}
    return {
        "name": spec.entry,
        "args": [],
        "kwargs": {
            "samples": samplelist.to_dicts(),
            "project_md": project_md or {},
            **{k: v for k, v in vals.items() if k != "arc_economy"},
        },
        "item_type": "plan",
    }


